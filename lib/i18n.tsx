const translations = { en, he } as const;

export type TranslationKey = keyof typeof en;

interface I18nContextType {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (k: TranslationKey | string) => string;
  isRTL: boolean;
}

const I18nContext = createContext<I18nContextType>({
  locale: 'en',
  setLocale: () => { },
  t: (k) => (en as Record<string, string>)[k as string] ?? (k as string),
  isRTL: false,
});

function detectLocale(): Locale {
  if (typeof window === 'undefined') return 'en';
  const saved = localStorage.getItem('trippy-locale');
  if (saved === 'he' || saved === 'en') return saved as Locale;
  const lang = navigator.language?.toLowerCase() ?? '';
  if (lang.startsWith('he') || lang.startsWith('iw')) return 'he';
  return 'en';
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('en');

  // Detect locale once on mount (client-side only)
  useEffect(() => {
    setLocaleState(detectLocale());
  }, []);

  const setLocale = (l: Locale) => {
    setLocaleState(l);
    if (typeof window !== 'undefined') localStorage.setItem('trippy-locale', l);
  };

  const isRTL = locale === 'he';
  const t = (k: TranslationKey | string): string => {
    const dict = translations[locale] as Record<string, string>;
    const fallback = en as Record<string, string>;
    return dict[k] ?? fallback[k] ?? k;
  };
  return (
    <I18nContext.Provider value={{ locale, setLocale, t, isRTL }}>
      {children}
    </I18nContext.Provider>
  );
}

export const useI18n = () => useContext(I18nContext);
